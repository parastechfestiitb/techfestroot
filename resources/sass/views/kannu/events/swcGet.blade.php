   			<h1 style="font-size: 2.8rem">How will the entries be judged?</h1>

   			<p class="lead">The entries will be evaluated by Fenox Capital and top 10 entries across India will be invited to Techfest to contest for entry into the Grand Finale</p>

   			<h1 style="font-size: 2.8rem">Will the airfare of the Indian Regional Winner be covered?</h1>

   			<p class="lead">Air tickets worth 60,000 INR will be sponsored by Techfest</p>

   			<h1 style="font-size: 2.8rem">What does the World Champion get for winning the final competition?</h1>

   			<p class="lead">The winner of the Grand Finale will receive US  $ 1,000,000 prize in investment. It will either be in exchange for a portion of equity related to the most recent round of funding or a convertible note, depending on the size of the company and how much funding they have raised.
			They will also receive global exposure through media recognition from all of the journalists covering the event. Selected sponsors will also be providing additional prizes to be announced. Last year included $30k in free legal services from Orrick and a Salesforce software package for the finalists
			</p>

   			<h1 style="font-size: 2.8rem">What type of startups can apply?</h1>

   			<p class="lead">We encourage startups in any domain, at any stage, to apply. Please note that the startup must be a legal entity not just an idea.</p>

   			

