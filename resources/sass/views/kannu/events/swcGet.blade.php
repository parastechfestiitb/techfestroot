</p>

   			<h1 style="font-size: 2.8rem">What is the registration fee?</h1>

   			<p class="lead">There is no fee for participating in Startup World Cup India Regional Finale</p>

   			<h1 style="font-size: 2.8rem">How many startups will be selected at each Regional Finale?</h1>

   			<p class="lead">​Top 10 startups selected among the applicants will be invited to Techfest, IIT Bombay to contest for Regional Winner</p>

   			<h1 style="font-size: 2.8rem">How long will each startup pitch for?</h1>

   			<p class="lead">​Each startup will be allocated 4 minutes to pitch followed by 2 minutes of Q&A from the judging panel.
