                self.ctx.globalCompositeOperation = 'lighter';
                self.updateFireworks();
                self.updateParticles();
                self.drawFireworks();
                self.drawParticles();
            };

            self.init();

            var initialLaunchCount = 10;
            while(initialLaunchCount--){
