            <div class="col-md-8 col-sm-offset-2 p-t-45 p-b-30">
                <div class="wrap-text-welcome t-center">
                    <p>
                        Osteoporosis is a disease in which the density and quality of bone are reduced, leading to weakness of the skeleton and increased risk of fracture, particularly of the spine, wrist and hip. Osteoporosis and associated fractures are an important cause of mortality and morbidity.
                    </p>
                    <p>
                        Osteoporosis is a global problem which is increasing in significance as the population of the world both grows and ages.

                    </p>
                    <p>
                        Worldwide, the lifetime risk for osteoporotic fractures in women is 30-50%. In men risk is 15-30%.

                    </p>
                </div>
            </div>
            <div class="col-md-4 col_width col-md-offset-0 p-t-45 p-b-30">
                <h2 style="color: #737692">
                    Symptoms
                </h2>
                <p>Osteoporosis is often called the "silent disease" because bone loss occurs without symptoms. In many cases, the first symptom is a broken bone. Patients with osteoporosis may not know that they have the disease until their bones become so weak that a sudden strain, bump, or fall causes a hip fracture or a vertebra to collapse.
                </p>
            </div>
            <div class="col-md-4 col_width p-t-45 p-b-30 col-md-offset-0">
                <h2 style="color: #737692">
                    Prevention
                </h2>
                <ul style="color: #666666" >
                    <li> Maintaining a healthy lifestyle means greater protection against fractures in later life.</li>
                    <li> Maintain a balanced diet to achieve adequate calcium and vitamin D intake.</li>
                    <li>Avoid smoking and high intakes of alcohol. </li>
                    <li> Take regular, weight-bearing exercise. Good bone building exercises include running, skipping, aerobics, tennis, and brisk walking. </li>
                </ul>
            </div>
            <div class="col-md-4 col_width p-t-45 p-b-30 col-md-offset-0">
                <h2 style="color: #737692">
                    Treatment
                </h2>
                <ul style="color: #666666" >
                    <li> Calcium and vitamin D supplements can be of benefit for older people to reduce the risk of hip fracture.
                    </li>
                    <li> Parathyroid hormone stimulates new bone formation and significantly increases bone mineral density and reduce fracture rates </li>
                    <li>Hormone replacement therapy (HRT) is estrogen replacement for women at the menopause, which help maintain bone density and reduce fracture rates for the duration of therapy.</li>
                </ul>
            </div>
        </div>
    </div>
